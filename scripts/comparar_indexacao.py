# Normalizar URLs (remover www ou adicionar, padronizar)
def normalizar(url):
    url = url.strip()
    url = url.replace('https://suplementaja.com', 'https://www.suplementaja.com')
    url = url.replace('http://', 'https://')
    return url

indexadas = set()
for url in indexadas_raw.strip().split('\n'):
    if url.strip():
        indexadas.add(normalizar(url))

print(f'URLs indexadas (normalizadas): {len(indexadas)}')

# Carregar todas as URLs do site
with open('todas_urls.txt', 'r', encoding='utf-8') as f:
    todas = set(line.strip() for line in f if line.strip())

print(f'URLs totais do site: {len(todas)}')

# Encontrar nao indexadas
nao_indexadas = todas - indexadas

print(f'\n=== PAGINAS NAO INDEXADAS ({len(nao_indexadas)}) ===\n')

# Categorizar
blog_nao_indexadas = []
nutrientes_nao_indexados = []
outras_nao_indexadas = []

for url in sorted(nao_indexadas):
    if '/blog/' in url:
        blog_nao_indexadas.append(url)
    elif '/nutrientes/' in url:
        nutrientes_nao_indexados.append(url)
    else:
        outras_nao_indexadas.append(url)

print('--- ARTIGOS DO BLOG NAO INDEXADOS ---')
for url in blog_nao_indexadas:
    print(url)

print('\n--- NUTRIENTES NAO INDEXADOS ---')
for url in nutrientes_nao_indexados:
    print(url)

print('\n--- OUTRAS PAGINAS NAO INDEXADAS ---')
for url in outras_nao_indexadas:
    print(url)

# Salvar lista para indexacao manual
with open('indexar_manualmente.txt', 'w', encoding='utf-8') as f:
    f.write('# ARTIGOS (PRIORIDADE ALTA)\n')
    for url in blog_nao_indexadas:
        f.write(url + '\n')
    f.write('\n# NUTRIENTES\n')
    for url in nutrientes_nao_indexados:
        f.write(url + '\n')
    f.write('\n# OUTRAS\n')
    for url in outras_nao_indexadas:
        f.write(url + '\n')

print(f'\n\nLista salva em indexar_manualmente.txt')
print(f'Total para indexar: {len(nao_indexadas)}')
